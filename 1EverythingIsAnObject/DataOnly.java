
public class DataOnly {
	  int i;    
	  double d;   
	  boolean b; 
	  public static void main(String[] args){
		  DataOnly data = new DataOnly();
		  data.i = 48;
		  data.d = 1.12;
		  data.b = false;
		  System.out.println(data.i);
		  System.out.println(data.b);
		  System.out.println(data.d);
	  }
}
