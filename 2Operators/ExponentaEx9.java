//Exercise 9: (1) Display the largest and smallest numbers 
//for both float and double exponential notation.
public class ExponentaEx9 {
	public static void main(String[] args) {
		System.out.println("Max float in exponentional notation is " + 3.4e38f);
		System.out.println("Min float in exponentional notation is " + -3.4E38F);
		System.out.println("Max double in exponentional notation is " + 1.7e308);
		System.out.println("Min double in exponentional notation is " + -1.7E308);
	}
}
